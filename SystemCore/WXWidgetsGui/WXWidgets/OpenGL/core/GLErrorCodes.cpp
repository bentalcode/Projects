/**
 * Gets an error message.
 */
std::wstring GLErrorCodes::GetErrorMessage(int errorCode)
{
    const CodeToMessageMap& codeToMessageMap = GetCodeToMessageMap();
    CodeToMessageMap::const_iterator messageIterator = codeToMessageMap.find(errorCode);
    return messageIterator->second;
}

/**
 * Gets the Code -> Message map.
 */
const GLErrorCodes::CodeToMessageMap& GLErrorCodes::GetCodeToMessageMap()
{
    static std::unique_ptr<CodeToMessageMap> mapPtr;

    if (!mapPtr) {
        mapPtr.reset(new CodeToMessageMap);
        InitializeCodeToMessageMap(*mapPtr);
    }

    return *mapPtr;
}

