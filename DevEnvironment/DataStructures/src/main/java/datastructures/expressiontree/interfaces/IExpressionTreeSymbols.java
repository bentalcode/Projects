package datastructures.expressiontree.interfaces;

/**
 * The IExpressionTreeSymbols interface defines symbols of an expression tree.
 */
public interface IExpressionTreeSymbols {
    char OpenParentheses = '{';
    char CloseParentheses = '}';
    char PowOperator = '^';
    char AddOperator = '+';
    char SubtractOperator = '-';
    char MultiplyOperator = '*';
    char DivideOperator = '/';
}
